def arith(num1,operator,num2):
    if operator== '+' :
        print(f"The result  is: {num1 + num2}")
    elif operator== '-':
        print(f"The result is: {num1 - num2}")
    elif operator=='*':
        print(f"The result is {num1 * num2}")
    elif operator=='/':
        print(f"The result is: {num1 / num2} " )
    
